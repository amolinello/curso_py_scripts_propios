
# Profundizando en el tipo float
a = 6.0
print(f'a: {a:.2f}')

# Constructor de tipo flotante
a = float(10)
print(f'a: {a:.5f}')

a = float('14')
print(f'a: {a:.4f}')

# Notacion exponencial, valores positivos o negativos
a = 3e-5
print(f'a: {a:.7f}')

# Cualquier operacion con float, se vuelve float
a = 1+2.0
print(f'a: {a}')
print(f'type(a): {type(a)}')

