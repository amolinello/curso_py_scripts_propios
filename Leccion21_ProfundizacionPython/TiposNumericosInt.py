# Profundizando sistemas de numeraci√≥n

# Decimal
a = 10
print(f'a: {a}')

# Binario
a = 0b1010
print(f'a: {a}')

# Octal
a = 0o12
print(f'a: {a}')

# Hexadecimal
a = 0xA
print(f'a: {a}')

# Convertir un tipo entero incluyendo la base
a = int('23', 10) # Base decimal, es base 10, es la que esta por default
print(f'a: {a}')

# Base binario
a = int('10111', 2)
print(f'a: {a}')

# Base Octal
a = int('27', 8)
print(f'a: {a}')

# Base Hexadecimal
a = int('17', 16)
print(f'a: {a}')

# Se puede pasar de cualquier tipo de base a entero

